package com.emergentmud.core.model;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.index.CompoundIndex;
import org.springframework.data.mongodb.core.index.CompoundIndexes;
import org.springframework.data.mongodb.core.mapping.DBRef;
import org.springframework.data.mongodb.core.mapping.Document;

@Document
@CompoundIndexes({
        @CompoundIndex(name = "location_idx", def = "{'x': 1, 'y': 1, 'z': 1}")
})
public class Entity {
    @Id
    private String id;
    private String name;
    private String stompUsername;
    private String stompSessionId;

    private Long x;
    private Long y;
    private Long z;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStompUsername() {
        return stompUsername;
    }

    public void setStompUsername(String stompUsername) {
        this.stompUsername = stompUsername;
    }

    public String getStompSessionId() {
        return stompSessionId;
    }

    public void setStompSessionId(String stompSessionId) {
        this.stompSessionId = stompSessionId;
    }

    public Long getX() {
        return x;
    }

    public void setX(Long x) {
        this.x = x;
    }

    public Long getY() {
        return y;
    }

    public void setY(Long y) {
        this.y = y;
    }

    public Long getZ() {
        return z;
    }

    public void setZ(Long z) {
        this.z = z;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof Entity)) return false;

        Entity entity = (Entity) o;

        return getId().equals(entity.getId());

    }

    @Override
    public int hashCode() {
        return getId().hashCode();
    }
}
